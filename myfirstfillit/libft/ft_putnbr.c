#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned nb;

	if (n < 0)
		ft_putchar('-');
	if (n < 0)
		n *= -1;
	nb = (unsigned)n;
	if (nb >= 10)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
		ft_putchar(nb + '0');
}
