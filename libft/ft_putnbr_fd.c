#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	unsigned nb;

	if (n < 0)
		ft_putchar_fd('-', fd);
	if (n < 0)
		n *= -1;
	nb = (unsigned)n;
	if (nb >= 10)
	{
		ft_putnbr_fd(nb / 10, fd);
		ft_putnbr_fd(nb % 10, fd);
	}
	else
		ft_putchar_fd(nb + '0', fd);
}
